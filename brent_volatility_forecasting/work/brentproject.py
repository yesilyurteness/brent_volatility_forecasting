# ── 3.2 Wavelet Denoising ────────────────────────────────────
# Fiyat serilerindeki kısa vadeli gürültüyü temizler, trendi korur.
#
# Neden train-fit?
#   Wavelet threshold = serinin standart sapmasından hesaplanır.
#   Tüm seri üzerinde hesaplasaydık test döneminin istatistikleri
#   train'e sızardı → data leakage.
#
# Hangi değişkenlere uygulanıyor?
#   Brent, GPRD, GPRD_THREAT → gürültülü, denoising faydalı
#   OVX → zaten volatilite endeksi, dokunmuyoruz

def wavelet_fit(series: pd.Series, wavelet: str = "db4") -> dict:
    """Train verisi üzerinde threshold parametresini hesapla."""
    data  = np.nan_to_num(series.values)
    coeff = pywt.wavedec(data, wavelet, mode="per")
    sigma = (1 / 0.6745) * np.median(np.abs(coeff[-1] - np.median(coeff[-1])))
    threshold = sigma * np.sqrt(2 * np.log(len(data)))
    return {"wavelet": wavelet, "threshold": threshold}


def wavelet_transform(series: pd.Series, params: dict) -> np.ndarray:
    """Verilen threshold ile seriyi denoise et."""
    data  = np.nan_to_num(series.values)
    coeff = pywt.wavedec(data, params["wavelet"], mode="per")
    coeff[1:] = [pywt.threshold(c, value=params["threshold"], mode="hard") for c in coeff[1:]]
    return pywt.waverec(coeff, params["wavelet"], mode="per")[:len(series)]


DENOISE_COLS   = ["Brent_Petrol", "GPRD", "GPRD_THREAT"]
