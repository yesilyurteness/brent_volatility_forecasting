# ── 4.11 Feature Winsorization + Scaling ─────────────────────
# Bölüm 3'teki winsorization yalnızca ham OVX/GPRD/GPRD_THREAT
# seviyelerine uygulanmıştı. Bölüm 4'te türetilen feature'lar
# (lag, EMA, z-score, etkileşim terimleri) henüz hiç işlenmedi.
#
# İki ayrı strateji:
#   Sürekli feature'lar → [1, 99] persentil winsorization → MinMaxScaler
#   Binary feature'lar  → winsorize etme, scale etme (zaten 0/1)


from sklearn.preprocessing import MinMaxScaler
from sklearn.metrics import mean_squared_error, mean_absolute_error, r2_score

BINARY_FEATS = ["ovx_spike", "ovx_high_regime", "gprd_spike",
                "is_month_end", "is_month_start"]

CONTINUOUS_FEATS = [f for f in FEATURES if f not in BINARY_FEATS]

# ── Winsorization (train-only) ──────────────────────────────
winsor_bounds = {}
for col in CONTINUOUS_FEATS:
    lo = np.percentile(X_train[col], 1.0)
    hi = np.percentile(X_train[col], 99.0)
    winsor_bounds[col] = (lo, hi)

def apply_winsor(df_part):
    out = df_part.copy()
    for col in CONTINUOUS_FEATS:
        out[col] = np.clip(out[col], *winsor_bounds[col])
    return out

X_train_w = apply_winsor(X_train)
X_val_w   = apply_winsor(X_val)
X_test_w  = apply_winsor(X_test)

# ── Scaling (train-only fit) ────────────────────────────────
scaler = MinMaxScaler()
scaler.fit(X_train_w[CONTINUOUS_FEATS])

def scale_df(df_part):
    out = df_part.copy()
    out[CONTINUOUS_FEATS] = scaler.transform(df_part[CONTINUOUS_FEATS])
    return out

X_train_s = scale_df(X_train_w)
X_val_s   = scale_df(X_val_w)
X_test_s  = scale_df(X_test_w)

y_train_arr = y_train.values
y_val_arr   = y_val.values
y_test_arr  = y_test.values

import joblib
joblib.dump(scaler, "scaler_features.pkl")

print("=" * 55)
print("  5.1 — Feature Winsorization + Scaling")
print("=" * 55)
print(f"  Sürekli feature (winsor + scale) : {len(CONTINUOUS_FEATS)}")
print(f"  Binary feature  (ham bırakıldı)  : {len(BINARY_FEATS)}")
print(f"  X_train_s : {X_train_s.shape}")
print(f"  X_val_s   : {X_val_s.shape}")
print(f"  X_test_s  : {X_test_s.shape}")
