# ── 3.3 Log Transform + Winsorization ───────────────────────
# OVX, GPRD, GPRD_THREAT sağa çarpık dağılım gösteriyor (EDA'da gördük).
#   1. Log transform  → dağılımı simetrikleştirir
#   2. Winsorization  → kalan aşırı değerleri kırpar (kriz spike'ları)
#
# Neden train-only?
#   Persentil sınırları train'den hesaplanır, val/test'e np.clip()
#   ile uygulanır. Test istatistikleri bu adıma karışmaz.

LOG_COLS = ["GPRD_clean", "GPRD_THREAT_clean", "OVX"]

winsor_bounds = {}
for col in LOG_COLS:
    log_vals = np.log1p(np.clip(df_train[col], 0, None))
    winsor_bounds[col] = (np.percentile(log_vals, 1.0), np.percentile(log_vals, 99.0))


def apply_log_winsor(df_part: pd.DataFrame) -> pd.DataFrame:
    out = df_part.copy()
    for col in LOG_COLS:
        log_vals = np.log1p(np.clip(df_part[col], 0, None))
        lo, hi   = winsor_bounds[col]
        fin_col  = col.replace("_clean", "_final") if "_clean" in col else col + "_final"
        out[fin_col] = np.clip(log_vals, lo, hi)
    return out


df_train = apply_log_winsor(df_train)
df_val   = apply_log_winsor(df_val)
df_test  = apply_log_winsor(df_test)

